package com.sweetk.kwu.common.vo;

public class CommonVo {
	String id				= "";
	String name				= "";
	String email			= "";
	String password			= "";
	String agree_yn			= "";
	String img_nm			= "";
	String img_org_nm		= "";
	String mem_prt			= "";
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAgree_yn() {
		return agree_yn;
	}
	public void setAgree_yn(String agree_yn) {
		this.agree_yn = agree_yn;
	}
	public String getImg_nm() {
		return img_nm;
	}
	public void setImg_nm(String img_nm) {
		this.img_nm = img_nm;
	}
	public String getImg_org_nm() {
		return img_org_nm;
	}
	public void setImg_org_nm(String img_org_nm) {
		this.img_org_nm = img_org_nm;
	}
	public String getMem_prt() {
		return mem_prt;
	}
	public void setMem_prt(String mem_prt) {
		this.mem_prt = mem_prt;
	}
	
	
}
